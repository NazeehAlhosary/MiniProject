    private void lblchangepassMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblchangepassMouseClicked

        if (!IsEmpty()){

            if (txtpass.getText().equals(txtrepass.getText())){
                PutInfo();
                if( customerlogin.Update() ){
                    customer.Update();
                    ClearInfo();
                    this.dispose();
                    Tools.OpenForm(new FrmCustomer_Login());
                }
            }
            else{
                Tools.MsgBoxErrorX("Please enter same password!", "Password invalid!");
                txtpass.setText("");
                txtrepass.setText("");
                txtpass.requestFocus();
            }
        }
        else{
            Tools.MsgBoxErrorI("Please fill all text fields and choose one question.", "Empty Error!");
        }
    }//GEN-LAST:event_lblchangepassMouseClicked

